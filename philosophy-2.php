            <p>Propositional Knowledge
            </p>
            <p>Personal knowledge is referred to as acquiring intellectual information through acquaintance and
                procedural knowledge is based completely on the process or technique with which a task or activity can
                be accomplished. On the other hand, propositional knowledge is completely different. It refers to the
                knowledge which depicts specific claims. For instance, I know that concerned project will be completed
                in two months. Knowledge basically reflects the understanding ability of a person. It can also be
                considered as the measure of being aware regarding a topic or subject. The individual that has personal
                knowledge is said to have propositional knowledge in some form. However, the vice versa situation is not
                necessary to exist. Several philosophers and researchers have depicted that all the three types of
                knowledge are interlinked and major epistemology has clear depiction of propositional knowledge.
                Difficulties in the perspective and examples It is often claimed that propositional knowledge majorly
                consists of true beliefs which are further supported with respective justifications. Great philosophers
                have classified propositional knowledge as the kind of ability which provides claims regarding
                proficiency in a subject. For instance, the project management is a strategic tool which involves all
                the attributes related with a project irrespective of the area of application. The planning tool
                requires concerned manager to be able to separate activities according to their priorities and then
                place them in the time scale. If a person claims that Gantt chart depicts the time scale of a project.
                Then it definitely refers to a propositional knowledge of the person. Considering another example: when
                a person states that he is aware about the objectives of plan then that person is actually claiming to
                posses propositional knowledge. True beliefs can be stated as truths only when they are supported or
                backed by appropriate evidences. These become a proof that the concerned fact or truth does exists and
                the fact stated by concerned individual holds certain type of validity. Epistemology is considered as an
                important part of the series based on philosophy. Great thinkers like Aristotle, Buddha, Plato, Kant,
                etc. had their philosophies in the epistemology. It can be considered as the study or research which
                comprises of justifications and knowledge that are aligned with rational beliefs. Any sort of knowledge
                can be considered as the same with validity and reliability in consideration, when there is
                justification, truth and belief. Scope of knowledge is also depicted through this understanding. A great
                difference can be experienced between knowing a concept and having an understanding of the operation or
                technique which needs to be followed. Furthermore, knowledge is also considered to be known through some
                relation or metaphorical capabilities. The conceptual knowledge can be utilised in application only when
                it is supported by certain type of justification. Similar can be considered as the case of propositional
                knowledge. If a person makes certain claims, then it can be contemplated as truth with help of
                respective justifications or proofs. Foundational-ism is one of the philosophical thesis which portrays
                the structuring of justification. One can understand this concept with help of the following example. If
                a person states that there is use of only internet based or online resources for the concerned project.
                Then it determines that the person justifiably believes that only online resources will be useful and no
                other source shall be required in the entire execution. The foundational justification of knowledge
                cannot be standardised proof but it justified in a sense that concerned fact does exists in the
                propositional form. Sufficient amount of valid reasons are required for developing a belief of
                propositional knowledge. There are two more theoretical concepts which can be considered for defying the
                statement that “knowledge comprises a true belief backed by a justification”. These include
                internalism-externalism. Internalism consists of the factors which are limited to the internal
                attributes of a person. On the other hand, externalism provides that justification for knowledge is
                completely based on the external and additional components which are experienced by the individual. This
                is quite a conflicting scenario. Often knowledge and belief are considered to be same. Belief can be
                stated as one’s faith in a subject or element irrespective of the fact whether it is backed by truth and
                justification. However, knowledge is a valid factual data always supported by appropriate
                justifications. When difficulties arise in this perspective then the issues are considered to be Gettier
                cases and Gettier problems (Hintikka, 2016). Despite of the fact that these require subsequent
                discussions in context of true belief and knowledge, but internal aspects of the person are expressed to
                be sufficient in justifying the knowledge. The major consequence which is experienced in the perspective
                acquired is the acquisition of belief. The claim made by one person may or may not be acceptable to
                other individual. For instance, projects hold many things at stake. The propositional knowledge turns
                out to be insufficient when decisions on budget, resources and time management have to be taken. If
                internalism is considered to be the only base on judging the decisions and knowledge then definitely
                conflicts might arise. Gettier problems will be addressed by the entire team. However, externalism

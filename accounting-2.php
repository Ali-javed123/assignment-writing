            <h5>Interpreting the technical requirements and importance of international accounting standards
            </h5>
            <p>International Accounting standards have high level of importance which ensures high level transparency and
                standardization in the financial statements. Hence, final accounts which are prepared according to IFRS are
                highly reliable in nature and thereby helps stakeholders in making decisions (Florou, Kosi and Pope, 2017).
                However, technical knowledge is highly required among the individuals or accounting personnel to deal with
                the aspects of International Accounting Standards such as IAS 12 and 26 etc. In this, accounting personnel
                must have ability to recognize deferred tax assets and liabilities. Moreover, assets, revenue received in
                advance, other liabilities, unrecognized items; consolidated financial statements are the main bases of
                taxation (Warren, 2016). By taking into consideration all such bases accounting personnel can assess or
                determine the tax liability. In addition to this, in IAS 12 contains rules regarding the measurement of
                deferred tax. Along with this, tax rate are also varied from one year to another. In this, accounting
                personnel is required to update with changing rules and policies. Through this, finance manager and
                accounting personnel would become able to determine suitable tax liability. In addition to this, IAS 26
                serves information about retirement plans which can be distinguished into two types such as defined benefit
                and contribution plan. Hence, it clearly presents information about the recording, measurement and
                disclosure of information regarding the retirement benefit plans. Hence, for effective presentation
                accounting personnel must have knowledge regarding the rules and regulations which are related to it (Bond,
                Govendir and Wells, 2016). Thus, by considering all such aspects it can be said that technical and
                conceptual knowledge is highly required for dealing with the standards more effectively and efficiently.
                Moreover, reporting and measurement aspect of pension plan is highly differing from one standard to another
                (McPhail, Macdonald and Ferguson, 2016). For instance: UK GAAP has different rules in relation to treatment
                on pension benefits in against to IAS. Hence, conceptual understanding and knowledge is highly required for
                dealing with the amount of retirement benefits and deferred tax amount. There are several key issues which
                are associated with financial reporting enumerated below:
            </p>
            <ul>
                <li>Reporting needs and requirements are mentioned in the regulations which business entity requires
                    following for ensuring transparency in the final accounts. However, sometimes is not possible for the
                    business organization to comply with all the rules to the large extent.
                </li>
                <li>Further, some government authorities have framed and introduced legislation which entail that companies
                    are not obliged to prepare financial statements according to accounting standards. Moreover, sometimes
                    treasurer directives also enforce accounting standard which company has to follow for the preparation of
                    financial statements.
                </li>
